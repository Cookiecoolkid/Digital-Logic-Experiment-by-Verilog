module barrelsft32(
      output reg signed [31:0] dout,
      input signed [31:0] din,
      input [4:0] shamt,     //移动位数
      input LR,           // LR=1时左移，LR=0时右移
      input AL            // AL=1时算术右移，AR=0时逻辑右移
	);
//add your code here
always @ (*) begin
  if(LR) begin
     if (AL) dout = din <<< shamt;
     else dout = din << shamt;
  end
  else begin
     if (AL) dout = din >>> shamt;
     else dout = din >> shamt;
  end
end
endmodule
